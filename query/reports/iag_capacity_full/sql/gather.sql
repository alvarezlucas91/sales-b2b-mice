  CAST(DD.anyo AS VARCHAR) + RIGHT('0' + CAST(DD.week AS VARCHAR), 2) AS [Flight YearWeek (YYYYWW)],
  DD.yearmon_str as [Flight YearMonth (YYYYMM)],
  DR.AT_DS_CLUSTER as [Cluster],
  DR.AT_DS_MARKET_RANKING as [Market Ranking],
  DR.AT_DS_AIRPORT_PAIR_ORDER as [Airport Pair Order],
  sum(FFF.km*FFF.seats) as [ASK]
FROM
   VUELING_CALCODS.dbo.FactFlight  FFF
   INNER JOIN VUELING_CALCODS.dbo.FactFlight FF ON (FFF.flight_sk=FF.flight_sk)
   INNER JOIN VUELING_data_master.dbo.DIM_ROUTE DR ON (FF.airport_orig=DR.AT_CD_ORIG and FF.airport_dest=DR.AT_CD_DEST)
   INNER JOIN VUELING_CALCODS.dbo.dimDate DD ON (DD.date=FF.flight_date)
   INNER JOIN ( 
  select ID_FLIGHT
	,OT_DT_START_DATE
	,OT_DT_END_DATE
	,AT_CD_FLIGHT_NUMBER
	,AT_CD_AIRLINE_CODE
	,AT_DT_FLIGHT_DATE_UTC
	,AT_DT_FLIGHT_DATE_LOCAL_ZONE
	,AT_CD_AIRPORT_ORIG_SCH
	,AT_CD_AIRPORT_DEST_SCH
	,AT_CD_AIRPORT_ORIG_OPER
	,AT_CD_AIRPORT_DEST_OPER
	,AT_CD_LEGCD
	,ID_ROUTE
	,ID_FLIGHT_STATUS
	,AT_CD_AIRLINE_CODE_MKT
	,AT_SCHEDULE_HOUR
	,ID_AIRCRAFT
	,AT_TS_STD_UTC
	,AT_TS_STA_UTC
	,AT_TS_ATA_NAV
	,AT_TS_ATA_AIMS
	,AT_TS_ATD_NAV
	,AT_TS_ATD_AIMS
