	,AT_TS_ORG_STD
	,AT_TS_STD
	,AT_TS_ORG_STA
	,AT_TS_STA
	,AT_TS_ETD
	,AT_TS_ETA
	,AT_TS_EOBT
	,AT_CD_FLIGHT_NUMBER_OPER
	,AT_ORD
	,AT_REAL_ORD
	,AT_CD_DEP_STAND
	,AT_CD_DEP_GATE
	,AT_CD_DIV_PORT
	,AT_IS_LEVEL
	,AT_IS_C3
	,AT_IS_C15
	,AT_IS_C60
	,AT_IS_C120
	,AT_IS_C180
	,AT_IS_C300
	,AT_IS_FW
	,AT_IS_OPERATIONAL
	,AT_CD_SOURCE
	,MT_SEATS
	,MT_DELAY_DEPARTURE
	,MT_BOARDED_PAX
	,AT_TS_TAKE_OFF
	,AT_TS_TAKE_DOWN
	,AT_TS_CLOSE_PAX_DOOR
	,AT_TS_CLOSE_CARGO_DOOR
	,MT_EXP_PAX
	,MT_REAL_PAX
	,MT_INF_PAX
	,MT_ST_ESCALA
	,MT_DELAY_ARR
	,MT_AT_ESCALA
	,MT_MIN_REGUL
	,MT_BAGS
	,MT_FUEL_KG
	,MT_BAGS_KG
	,MT_TOTAL_DLYT
	,MT_DISTANCE
	,MT_ASKS
	,CA_PAX_SOLD
	,CA_PAX_FLOWN
	,CA_REVENUE_TICKETS
