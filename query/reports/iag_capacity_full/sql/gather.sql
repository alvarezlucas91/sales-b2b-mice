	,AT_IS_OPERATED
	,AT_DT_FLIGHT_CANCEL, ID_FLIGHT_TYPE

from VUELING_data_master.dbo.fact_flight_global
where ot_Dt_end_date='9999-12-31'
 
 
  )  FACT_FLIGHT_GLOBAL ON (FACT_FLIGHT_GLOBAL.ID_FLIGHT=FF.flight_sk)
WHERE
  FF.idStatus=1
  AND FF.airline_code = 'VY'
  AND FF.flight_number NOT BETWEEN '4000' AND '4999'
  AND case when FACT_FLIGHT_GLOBAL.ID_FLIGHT_STATUS IN(9, 21,  25, 27, 63) and (FACT_FLIGHT_GLOBAL.ID_FLIGHT_TYPE in (1,2,8) or (FACT_FLIGHT_GLOBAL.ID_FLIGHT_TYPE is null and FACT_FLIGHT_GLOBAL.at_dt_flight_date_local_zone>=cast(getdate() as date))) then 0 else 1 end  IN  ( 0 )
  AND (DD.ANYO  >= '2019' AND DD.WEEK >= 1)
GROUP BY
  DD.ANYO,
  DD.week, 
  DD.yearmon_str, 
  DR.AT_DS_CLUSTER, 
  DR.AT_DS_MARKET_RANKING,
  DR.AT_DS_AIRPORT_PAIR_ORDER